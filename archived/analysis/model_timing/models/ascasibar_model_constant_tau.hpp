/* Constants. */

const Double tau_S = 2.6;		  // [Gyr]
const Double C1 = 0.074;		  // [Mₒ^2 pc^(-4) Gyr]
const Double C2 = 360e-3;		  // [Mₒ^2 pc^(-4) Gyr]
